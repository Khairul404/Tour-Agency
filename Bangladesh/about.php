        }

        // SQL to create table if it doesn't exist
        $createTableSQL = "
        CREATE TABLE IF NOT EXISTS visitors (
            id INT AUTO_INCREMENT PRIMARY KEY,
            name VARCHAR(255) NOT NULL,
            email VARCHAR(255) NOT NULL,
            phone VARCHAR(15) NOT NULL,
            message TEXT NOT NULL,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        );";

        if ($conn->query($createTableSQL) !== TRUE) {
            die("<script>alert('Error creating table: " . $conn->error . "');</script>");
        }

        // Retrieve form data
        $name = $_POST['name'];
        $email = $_POST['email'];
        $phone = $_POST['phone'];
        $message = $_POST['message'];

        // Validate email address
        if (!filter_var($email, FILTER_VALIDATE_EMAIL)) {
            die("<script>alert('Error: Invalid email address');</script>");
        }

        // Validate phone number
        if (!preg_match("/^\d{11}$/", $phone)) {
            die("<script>alert('Error: Invalid phone number');</script>");
        }

        // Prepare and bind
        $stmt = $conn->prepare("INSERT INTO visitors (name, email, phone, message) VALUES (?, ?, ?, ?)");
        if ($stmt === false) {
            die("<script>alert('Error preparing statement: " . $conn->error . "');</script>");
        }

        $stmt->bind_param("ssss", $name, $email, $phone, $message);

        // Execute the prepared statement
        if ($stmt->execute()) {
            echo "<script>alert('Sent Successfully!');</script>";
        } else {
            echo "<script>alert('Error: " . $stmt->error . "');</script>";
        }

